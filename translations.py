"""Translation strings for the Streamlit dashboard.

Usage:
    from translations import TRANSLATIONS, t, tl
    # t("key") returns the translated string for the current session language.
    # tl("key", lang) returns the translated string for a specific language.
"""

import streamlit as st


def t(key):
    """Return translated string for current session language."""
    lang = st.session_state.get("lang", "en")
    return TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(key, TRANSLATIONS["en"].get(key, key))


def tl(key, lang):
    """Return translated string for a specific language."""
    return TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(key, TRANSLATIONS["en"].get(key, key))


